    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
        model.setRowCount(0);
        for (Customer c : business.getOrgDirectory().getCustomerOrganization().getAllCustomer()) {
            Object[] row = new Object[5];
            row[0] = c.getUserId();
            row[1] = c;
            row[2] = c.getAge();
            row[3] = c.getLocationArea();
            row[4] = c.getAddress();
            model.addRow(row);
        }
    }

