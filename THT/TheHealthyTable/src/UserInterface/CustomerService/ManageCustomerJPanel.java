    private void btDisableCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDisableCustomerActionPerformed
        // TODO add your handling code here:
        int row = tblCustomer.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "PLease select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Customer c = (Customer) tblCustomer.getValueAt(row, 1);
        
        c.getUserAccount().setStatus(UserAccount.AccountStatusType.Inactive);
        DatabaseFactory.getInstance().storeSystem();
        JOptionPane.showMessageDialog(null, "Disabled");
        populateTable();
    }//GEN-LAST:event_btDisableCustomerActionPerformed

