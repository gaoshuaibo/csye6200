    private void btOnlineSumbitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOnlineSumbitActionPerformed
        // TODO add your handling code here:
        String cardNumber = txtCardNumber.getText().trim();
        String code = txtCode.getText().trim();
        String month = txtMonth.getText().trim();
        String year = txtYear.getText().trim();

        if (cardNumber.equals("") || code.equals("") || month.equals("") || year.equals("")) {
            JOptionPane.showMessageDialog(null, "Invalid input");
            return;
        }

        CardVerification cardVerification = CardVerification.getInstance();
        if (cardVerification.verifyCard(cardNumber, code, month, year)) {
            payment.payOnline();
            DatabaseFactory.getInstance().storeSystem();
            JOptionPane.showMessageDialog(null, "Order placed.");
            parent.populateOrder();
            userProcessContainer.remove(this);
            ((CardLayout) userProcessContainer.getLayout()).previous(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Card verification failed.");
            return;
        }

    }//GEN-LAST:event_btOnlineSumbitActionPerformed

