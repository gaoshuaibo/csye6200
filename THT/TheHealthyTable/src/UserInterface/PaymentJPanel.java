    public void populateOrderTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        model.setRowCount(0);
        for (OrderItem oi : payment.getOrder().getOrderItemList()) {
            Object[] row = new Object[3];
            row[0] = payment.getOrder().getRestaurant().getName();
            row[1] = oi.getDish().getName();
            row[2] = oi.getQuantity();
            model.addRow(row);
        }
    }

    public void populateInfo() {
        txtBalance.setText(String.valueOf(payment.getBalance()));
        txtActuralPayment.setText(String.valueOf(payment.getBalance() * payment.getDiscount()));
        txtPayAtRestaurantBalance.setText(String.valueOf(payment.getBalance()));
    }

