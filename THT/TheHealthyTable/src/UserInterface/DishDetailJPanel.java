    public void populateInfo() {
        icon = new ImageIcon(dish.getImagePath());
        lbImage.setIcon(icon);
        txtDishName.setText(dish.getName());
        txtPrice.setText(String.valueOf(dish.getPrice()));
        txtRestaurant.setText(dish.getRestaurant().getName());
        txtCalorie.setText(String.valueOf(dish.getCalorieBalance()));
        populateIngredientTable();
    }

    public void populateIngredientTable() {
        DefaultTableModel model = (DefaultTableModel) tblIngredient.getModel();
        model.setRowCount(0);
        for (Ingredient i : dish.getIngredientDirectory().getIngredientList()) {
            Object[] row = new Object[2];
            row[0] = i.getName();
            row[1] = i.getAmount();
            model.addRow(row);
        }
    }

