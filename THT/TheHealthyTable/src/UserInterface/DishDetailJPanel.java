    private void btAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddToCartActionPerformed
        // TODO add your handling code here:
        if (this.cart == null) {
            JOptionPane.showMessageDialog(null, "Please Log IN");
            return;
        }
        int quantity = 0;
        try {
            quantity = Integer.parseInt(txtQuantity.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Invalid input.");
            return;
        }
        CartItem cartItem = this.cart.addCartIteam(dish.getRestaurant());
        cartItem.addCartItemUnit(dish, quantity);
        DatabaseFactory.getInstance().storeSystem();
        JOptionPane.showMessageDialog(null, "Added to cart");
    }//GEN-LAST:event_btAddToCartActionPerformed


