    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        // TODO add your handling code here:
        float budgetMin = 0;
        float budgetMax = 0;
        if (rd0_10.isSelected()) {
            budgetMin = 0;
            budgetMax = 9;
        } else if (rd10_20.isSelected()) {
            budgetMin = 10;
            budgetMax = 20;
        } else if (rd21_30.isSelected()) {
            budgetMin = 21;
            budgetMax = 30;
        } else if (rd31_40.isSelected()) {
            budgetMin = 31;
            budgetMax = 40;
        } else if (rd41_50.isSelected()) {
            budgetMin = 41;
            budgetMax = 50;
        } else if (rd51_100.isSelected()) {
            budgetMin = 51;
            budgetMax = 100;
        } else {
            budgetMin = 101;
            budgetMax = 9999;
        }
        Survey.TrendType trendType;
        if (radioLoseWeight.isSelected()) {
            trendType = Survey.TrendType.LoseWeight;
        } else if (radioGainWeight.isSelected()) {
            trendType = Survey.TrendType.GainWeight;
        } else {
            trendType = Survey.TrendType.Remain;
        }

        Survey survey = null;
        if (customer.getSurvey() == null) {
            survey = new Survey();
            customer.setSurvey(survey);
        } else {
            survey = customer.getSurvey();
        }
        survey.setBudgetMin(budgetMin);
        survey.setBudgetMax(budgetMax);
        survey.setTrent(trendType);

        survey.getEatingHabbit().getIngredientTypeList().clear();
        List<Ingredient.IngredientType> habbitList = listHabbit.getSelectedValuesList();
        for (Ingredient.IngredientType type : habbitList) {
            survey.getEatingHabbit().getIngredientTypeList().add(type);
        }

        survey.getAllergy().getIngredientTypeList().clear();
        List<Ingredient.IngredientType> allergyList = listAllergy.getSelectedValuesList();
        for (Ingredient.IngredientType type : allergyList) {
            survey.getAllergy().getIngredientTypeList().add(type);
        }
        DatabaseFactory.getInstance().storeSystem();
        JOptionPane.showMessageDialog(null, "Thank you. Now you can go to your diet.");

//        userProcessContainer.remove(this);
//        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
//        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btSubmitActionPerformed


