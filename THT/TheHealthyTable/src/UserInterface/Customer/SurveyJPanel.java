    private void populatePreviousSurvey() {
        float budgetMin = customer.getSurvey().getBudgetMin();
        if (budgetMin == 0) {
            rd0_10.setSelected(true);
        } else if (budgetMin == 10) {
            rd10_20.setSelected(true);
        } else if (budgetMin == 21) {
            rd21_30.setSelected(true);
        } else if (budgetMin == 31) {
            rd31_40.setSelected(true);
        } else if (budgetMin == 41) {
            rd41_50.setSelected(true);
        } else if (budgetMin == 51) {
            rd51_100.setSelected(true);
        } else if (budgetMin == 101) {
            rd101_more.setSelected(true);
        }

        Survey.TrendType trendType = customer.getSurvey().getTrent();
        if (trendType == TrendType.LoseWeight) {
            radioLoseWeight.setSelected(true);
        } else if (trendType == TrendType.GainWeight) {
            radioGainWeight.setSelected(true);
        } else {
            radioImGood.setSelected(true);
        }

        DefaultListModel listModel = (DefaultListModel) listHabbit.getModel();
        int[] eatingHabbitIndices = new int[customer.getSurvey().getEatingHabbit().getIngredientTypeList().size()];
        int index = 0;
        for (Ingredient.IngredientType type : customer.getSurvey().getEatingHabbit().getIngredientTypeList()) {
            eatingHabbitIndices[index] = listModel.indexOf(type);
            index++;
        }
        listHabbit.setSelectedIndices(eatingHabbitIndices);

        listModel = (DefaultListModel) listAllergy.getModel();
        int[] allergyIndices = new int[customer.getSurvey().getAllergy().getIngredientTypeList().size()];
        index = 0;
        for (Ingredient.IngredientType type : customer.getSurvey().getAllergy().getIngredientTypeList()) {
            allergyIndices[index] = listModel.indexOf(type);
            index++;
        }
        listAllergy.setSelectedIndices(allergyIndices);
    }

    public void populateEatingHabbitArea() {
        DefaultListModel model = new DefaultListModel();
        listHabbit.setModel(model);
        for (Ingredient.IngredientType type : Ingredient.IngredientType.values()) {
            model.addElement(type);
        }
    }

    public void populateAllergyArea() {
        DefaultListModel model = new DefaultListModel();
        listAllergy.setModel(model);
        for (Ingredient.IngredientType type : Ingredient.IngredientType.values()) {
            model.addElement(type);
        }
    }

